//lhgdialog弹出层加载响应脚本，并设置弹出层使用的样式
(function () {
    var _skin, _lhgcore;
    var _search = window.location.search;
    if (_search) {
        _skin = _search.split('demoSkin=')[1];
    };

    document.write('<scr' + 'ipt src="Plugin/lhgdialog/lhgdialog.min.js?skin=discuz"></sc' + 'ript>');
    window._isDemoSkin = !!_skin;
})();
